def detect_nail_region(img):
    hsv = cv2.cvtColor(img, cv2.COLOR_BGR2HSV)
    mask = cv2.inRange(hsv, np.array([0,10,60]), np.array([30,150,255]))
    k = np.ones((7,7), np.uint8)
    mask = cv2.morphologyEx(cv2.morphologyEx(mask, cv2.MORPH_CLOSE, k), cv2.MORPH_OPEN, k)
    cnts, _ = cv2.findContours(mask, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
    h_i, w_i = img.shape[:2]
    if not cnts: return 0,0,w_i,h_i
    c = max(cnts, key=cv2.contourArea)
    if cv2.contourArea(c) < 0.05*(w_i*h_i): return 0,0,w_i,h_i
    x,y,w,h = cv2.boundingRect(c)
    mx,my = int(w*0.1), int(h*0.1)
    x,y = max(0,x-mx), max(0,y-my)
    w,h = min(w_i-x,w+2*mx), min(h_i-y,h+2*my)
    return x,y,w,h

def enhance_image(img):
    lab = cv2.cvtColor(img, cv2.COLOR_BGR2LAB)
    l,a,b = cv2.split(lab)
    l = cv2.createCLAHE(clipLimit=2.5, tileGridSize=(8,8)).apply(l)
    enh = cv2.cvtColor(cv2.merge((l,a,b)), cv2.COLOR_LAB2BGR)
    den = cv2.fastNlMeansDenoisingColored(enh, None, 3,3,7,21)
    return cv2.filter2D(den, -1, np.array([[0,-1,0],[-1,5,-1],[0,-1,0]]))

def preprocess_nail_image(path, size=(224,224)):
    img = cv2.imread(path)
    x,y,w,h = detect_nail_region(img)
    crop = img[y:y+h, x:x+w]
    final = cv2.resize(enhance_image(crop), size)
    return img, crop, final

