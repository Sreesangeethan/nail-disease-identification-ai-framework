def make_gradcam_heatmap(img_array, model, layer_name="Conv_1"):
    grad_model = tf.keras.models.Model([model.inputs], [model.get_layer(layer_name).output, model.output])
    with tf.GradientTape() as tape:
        conv_out, preds = grad_model(img_array)
        idx = tf.argmax(preds[0])
        class_ch = preds[:, idx]
    grads = tape.gradient(class_ch, conv_out)
    pooled = tf.reduce_mean(grads, axis=(0,1,2))
    heatmap = conv_out[0] @ pooled[..., tf.newaxis]
    heatmap = tf.squeeze(heatmap)
    heatmap = tf.maximum(heatmap,0)/(tf.reduce_max(heatmap)+1e-8)
    return heatmap.numpy(), class_names[idx.numpy()]

def show_gradcam(path, model):
    img = tf.keras.preprocessing.image.load_img(path, target_size=IMG_SIZE)
    arr = tf.keras.preprocessing.image.img_to_array(img)
    batch = np.expand_dims(preprocess_input(arr.copy()), axis=0)
    hm, pred = make_gradcam_heatmap(batch, model)
    hm_r = tf.image.resize(hm[...,np.newaxis], IMG_SIZE).numpy().squeeze()
    overlay = np.clip(plt.cm.jet(hm_r)[...,:3]*255*0.4 + arr, 0, 255).astype("uint8")
    fig, ax = plt.subplots(1,2, figsize=(8,4))
    ax[0].imshow(arr.astype("uint8")); ax[0].set_title("Original"); ax[0].axis("off")
    ax[1].imshow(overlay); ax[1].set_title(f"Grad-CAM: {pred}"); ax[1].axis("off")
    plt.tight_layout(); plt.show()

